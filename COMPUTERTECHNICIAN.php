            Profesi seorang teknisi komputer dikelompokkan menjadi 4 cabang. Diantaranya yaitu perbaikan, server, infrastruktur dan juga jaringan.<br>
            1. Teknisi Komputer Perbaikan<br>
            Teknisi perbaikan adalah profesi teknisi komputer yang berkaitan dengan perbaikan pada perangkat-perangkat keras. Misalnya yaitu melakukan perbaikan pada monitor, printer, keyboard, CPU dan lain-lain.

            Teknisi perbaikan biasanya melakukan pembongkaran pada unit perangkat keras. Selanjutnya mereka akan mencari komponen-komponen yang rusak dan menggantinya dengan yang baru agar unit komputer bisa kembali dioperasikan.<br><br>

            2. Teknisi Komputer Server<br>
            Selanjutnya, ada juga teknisi yang khusus mengelola masalah server. Teknisi komputer ini memiliki tugas yang berkaitan dengan menejemen server, seperti, scooping, instalasi, konfigurasi, dan mengatasi permasalahan-permasalahan yang berkaitan dengan sistem server.

            Teknisi server juga bertugas untuk melakukan perawatan terhadap sistem jaringan internal yang digunakan pada sebuah instansi atau perusahaan.
            Berkat bantuan teknisi tersebut, sistem server tetap dalam keadaan prima dan siap menunjang produktivitas karyawan dalam perusahaan tersebut.<br><br>

            3. Teknisi Komputer Infrastruktur<br>
            Teknisi komputer infrastruktur merupakan profesi teknisi yang berfokus pada hal-hal yang berkaitan dengan infrastruktur.

            Salah satunya yaitu penentuan tempat atau tata letak dimana instalasi dan jaringan komputer akan dipasang nantinya.<br><br>

            4. Teknisi Komputer Jaringan<br>
            Teknisi komputer jaringan adalah orang yang bertanggung jawab dengan sistem instalasi jaringan.

            Jadi teknisi ini tugasnya untuk merakit sistem instalasi jaringan, melakukan pemeliharaan, melakukan pemantauan pada aktivitas jaringan, melakukan pengelolaan pada server printer dan lain sebagainya.
