            Apa Itu SSD ?<br>
            SSD adalah singkatan dari Solid State Drive yang merupakan salah satu jenis media penyimpanan data elektronik. Secara singkat, pengertian SSD adalah perangkat keras penyimpanan data yang menggunakan non-volatile memory sebagai media penyimpanannya dan tidak menggunakan piringan magnet seperti harddisk konvensional. Berbeda dengan volatile memory pada RAM, data yang tersimpan pada SSD tidak akan hilang meskipun tidak menggunakan daya listrik.<br><br>
            Komponen SSD
            Sama seperti perangkat keras lainnya, SSD adalah media penyimpanan yang juga memiliki komponen penyusun penting agar mekanisme kerja alatnya berjalan dengan mulus. Adapun komponen SSD adalah sebagai berikut.<br>

            1. Kontroler<br>
            Komponen pertama dari SSD adalah kontroler. Komponen ini berfungsi sebagai pengontrol dan menyediakan fitur antarmuka tingkat perangkat SSD dan juga mengeksekusi firmware.
            <br><br>
            2. Addressing<br>
            Berikutnya yaitu addressing. Komponen SSD ini merupakan logika yang berfungsi untuk menjalankan pemilihan di seluruh komponen memori flash.
            <br><br>
            3. Flash Memory-Based<br>
            Komponen selanjutnya yaitu flash memory-based. Biasanya, komponen ini digunakan oleh pabrik-pabrik besar untuk pembuatan SSD karena dibandingkan dengan bahan DRAM, komponen berikut memiliki harga bahan yang lebih murah.

            Adapun fungsi flash memory based pada SSD adalah mempertahankan data tanpa adanya suplai daya secara terus menerus.
            <br><br>
            4. Data Buffer/Cache<br>
            Komponen data buffer/cache merupakan RAM yang memiliki kecepatan tinggi. Biasanya komponen berikut digunakan untuk mencocokan kecepatan dan peningkatan throughput data.<br><br>
            5. DRAM-Based<br>
            Komponen DRAM-Based digunakan untuk pembuatan SSD yang menggunakan baterai internal atau arus listrik AC/DC adapter dan sistem backup penyimpanan untuk mempertahankan persistensi data selama tidak adanya daya listrik yang mengalir.<br><br>

            6. Baterai<br>
            Komponen terakhir SSD adalah baterai, yang digunakan untuk meningkatkan performa dari hardware penyimpanan. Baterai ini dibutuhkan SSD yang memiliki jenis MLC (Multi Level Cell) karena data rentan korup ketika listrik padam. Sedangkan untuk SSD bertipe SLC (Single Level cell) tidak berpotensi korup data sehingga mayoritas tidak dilengkapi baterai atau super kapasitor.
            <br><br>
            Fungsi SSD<br>
            Utamanya, fungsi SSD adalah membaca dan menulis data serta memelihara data yang disimpan dalam keadaan permanen bahkan tanpa daya.

            Sementara itu, ada beberapa fungsi SSD eksternal dan internal lainnya yang bisa kamu gunakan untuk berbagai situasi. Berikut di antaranya.
            <br><br>
            1. Pekerjaan<br>
            Fungsi SSD yang pertama bisa membantu kamu dalam hal pekerjaan. Yup, kamu dapat menggunakan SSD untuk menyimpan banyak data di laptop maupun komputer. Proses pembacaan dan transfer datanya yang cepat tentunya akan mempermudah kamu dalam bekerja.
            <br><br>
            2. Main game<br>
            Kamu hobi bermain game? Jika iya, maka fungsi SSD bisa kamu manfaatkan untuk membuat pengalaman main game-mu jadi lebih menyenangkan. Pasalnya, fungsi SSD adalah dapat mempercepat proses dalam mengirimkan data sehingga kamu tak perlu khawatir perangkat jadi lemot.
            <br><br>
            3. Laptop atau komputer<br>
            Fungsi SSD lainnya adalah membantu laptop kamu agar tidak kehabisan baterai ketika digunakan. Selain itu, SSD juga dikenal sebagai alternatif penyimpanan yang shock resistance atau menahan guncangan sehingga akan meminimalisir risiko kehilangan data.
            <br><br>
            4. Server<br>
            Buat kamu yang sedang bekerja di bidang server, pasti membutuhkan proses pembacaan dan juga pengiriman data dengan cepat agar bisa memberikan pelayanan terbaik kepada klien. Tapi kamu tak perlu khawatir, sebab fungsi SSD yang mampu mempercepat proses pengiriman data dapat memenuhi kebutuhan kamu.<br><br>
            Manfaat SSD<br>
            Selain fungsi di atas, SSD juga memiliki manfaat dalam bidang-bidang tertentu. Manfaat SSD adalah sebagai berikut.<br>

            1. Bisnis<br>
            Salah satu bidang yang terbantu dengan adanya SSD adalah bisnis. Dalam bisnis, pastinya mereka bekerja dengan data dalam jumlah besar. Sehingga dengan kebutuhan tersebut, suatu perusahaan atau bisnis biasanya membutuhkan SSD untuk mempercepat akses karena bagi mereka kecepatan transfer file dinilai penting.<br><br>

            2. Mobilitas<br>
            Manfaat SSD berikutnya adalah bermanfaat untuk mobilitas. SSD memiliki kebutuhan daya yang rendah dan tahan terhadap guncangan. Dengan demikian, penggunaan SSD bisa meminimalisir kehilangan data sehingga cocok digunakan bagi mereka yang mobilitasnya tinggi.<br><br>

            3. Gaming<br>
            Bagi seorang gamers, manfaat SSD juga sangat penting. Biasanya komputer yang digunakan untuk bermain games membutuhkan peralatan tambahan yang relatif mahal untuk menghasilkan performa game yang berkualitas tinggi.<br><br>

            4. Server<br>
            Pada server perusahaan, SSD bermanfaat untuk mempercepat dalam melayani kebutuhan komputer atau laptop klien.<br><br>

            Cara Kerja SSD<br>
            Cara kerja SSD adalah membaca dan menulis data ke chip memori flash yang saling berhubungan. Chip memori flash ini biasanya terbuat dari silikon. Pembuat SSD akan menumpuk chip dalam kotak untuk menghasilkan kepadatan yang berbeda.

            Chip yang dimaksud dalam SSD tersebut menggunakan floating gate transistors (FGT) untuk menahan muatan listrik. Berikutnya SSD akan digunakan untuk menyimpan data meskipun tidak terhubungan ke sumber tenaga.

            Sebagai tambahan, floating gate transistors (FGT) biasanya berisi satu bit data, kemudian ditunjuk sebagai 1 untuk sel yang bermuatan atau 0 jika tidak ada daya listrik. Nah, kira-kira seperti itulah cara kerja SSD secara singkat.<br><br>

            Jenis-jenis SSD<br>
            Jenis-jenis SSD card ditentukan dari 3 form faktor, yaitu ukuran fisik, jenis koneksi interface dan luas ruangnya. Berikut ini adalah jenis SSD yang perlu kamu ketahui berdasarkan dimensi fisik dan konektor interfacenya.<br>

            1. NVMe<br>
            Jenis SSD yang pertama adalah NVMe. NVMe merupakan jenis SSD generasi terbaru yang menggunakan slot M.2. NVMe menggunakan bus yang digunakan PCle dengan controller NVMe (Non Volatile Memory express). Ukuran fisik NVMe juga cukup bermacam-macam, namun memiliki panjang tetap, yaitu 22 mm. Kecepatan jenis SSD ini bisa mencapai batas maksimal 3200 MB/s.<br><br>

            2. SATA<br>
            SATA adalah jenis SSD yang memiliki kecepatan transfer data maksimum hingga 6 Gbps. SATA termasuk jenis SSD paling rendah dalam hal performa karena menggunakan antarmuka sama seperti hard drive. SSD berbasis SATA ini memiliki kelebihan dalam hal bandwidth tiga hingga empat kali lipat dibandingkan hard drive spinning disk.<br><br>

            3. PCIe<br>
            PCle adalah salah satu jenis SSD yang memiliki performa lebih baik dibandingkan SSD berbasis SATA. Spesifikasi format yang digunakan SSD PCle dikelola olah PCI Special Interest Group.<br><br>

            4. M2 Card<br>
            M2 Card adalah salah satu jenis SSD yang memenuhi spesifikasi M2. Awalnya nama SSD ini adalah Next Generation Form Factor, namun kemudian diganti menjadi M2. SSD M2 Card memiliki dimensi 2280, dengan lebar 22 mm dan panjang 80 mm.<br><br>

            Perbedaan HDD dan SSD<br>
            Setelah membahas mengenai pengertian SSD di atas, kita akan beralih pada perbedaannya dengan HDD. Secara umum, HDD adalah perangkat keras penyimpanan data sekunder yang berisi piringan magnetis. Bila diuraikan, perbedaan HDD dan SSD adalah sebagai berikut.<br>

            1. Kapasitas maksimum<br>
            Pertama, perbedaan HDD dan SSD adalah dilihat dari kapasitas maksimumnya. Jarang ditemukan SSD yang memiliki kapasitas hingga 2TB. Kamu akan lebih banyak menemukan SSD dengan kapasitas 500GB hingga 1TB sebagai drive utama dalam sistem. Sedangkan HDD punya kapasitas yang lebih besar, bahkan 500GB HDD saja dianggap sebagai kapasitas paling dasar dan paling rendah.<br><br>

            2. Kecepatan<br>
            Perbedaan HDD dan SSD berikutnya adalah dari segi kecepatan. Dalam hal ini, SSD lebih unggul. Dengan menggunakan SSD, proses booting tidak akan memakan waktu lebih lama dari satu menit, bahkan hanya sampai dalam hitungan detik. Sedangkan pada HDD diperlukan waktu lebih lama untuk proses booting hingga membuka aplikasi.<br><br>

            3. Keandalan dan Daya Tahan<br>
            Dalam hal daya tahan, SSD mempunyai ketahanan yang lebih handal. Hal ini karena SSD tidak memiliki bagian yang bergerak, jadi lebih aman dan sistem tidak gampang terguncang saat sedang dioperasikan. Berbeda dengan HDD yang berpotensi lebih besar untuk kehilangan data ketika laptop atau komputer terjatuh, sehingga dalam hal ketahanan HDD tidak cocok digunakan untuk aktivitas mobilitas tinggi.<br><br>

            4. Harga<br>
            Perbedaan HDD dan SSD yang terakhir yakni berkaitan dengan harga. Yup, nyatanya keduanya memiliki perbedaan harga cukup jauh. Harga SSD cenderung lebih mahal dibandingkan HDD. Sebagai contoh, harga SSD untuk laptop dengan kapasitas berukuran 1TB sekitar Rp1.700.000, sedangkan HDD dengan kapasitas yang sama berkisar Rp560.000 saja.
