            Apa itu Keyboard ?<br>
            Secara umum pengertian Keyboard merupakan hardware atau perangkat keras yang berguna sebagai alat dalam memasukan data yang terdiri dari berupa huruf, angka dan simbol-simbol pendukung.

            Memiliki bentuk fisik seperti papan dan di atasnya terdapat berbagai macam tombol. Dan tombol tersebut berguna sebagai mana fungsinya ,tergantung dengan penekanan yang di inginkan oleh penggunanya atau user.
            Tidak hanya sebagai alat perangkat keras untuk memasukan data, Keyboard juga memiliki fungsi lainnya yang tidak kalah penting.

            Pada Keyboard juga terdapat tombol-tombol kombinasi yang memudahkan kita dalam mengoperasikan sebuah komputer.

            Seiring kemajuan zaman, Keyboard juga mengalami perubahan yang signifikan. Mulai dari tampilan hingga bentuk fisik yang semakin ringan dan kecil.<br><br>

            Fungsi keyboard<br>
            Pada dasarnya fungsi keyboard yang utama adalah mengirimkan intruksi dari setiap huruf, angka dan simbol yang ditekan oleh pengguna.

            Kemudian intruksi tersebut disampaikan ke CPU. Intruksi tersebut juga diolah menjadi sinyal digital sehingga dapat dibaca oleh Processor.

            Dari data yang telah dibaca oleh Processor, selanjutnya Processor akan menampilkan data tersebut kedalam monitor atau layar komputer.
            Jenis-Jenis Keyboard
            Jenis keyboard yang beragam macam dan bervarian, mulai dari bentuk fisiknya hingga fungsi Keyboard pada sebuah komputer.

            Membuat perangkat keras berupa Keyboard memiliki banyak keunggulan sesuai dengan fungsinya. Ada yang berbentuk Keyboard Qwerty, Maltron, Dvorak, Alphabetic, Klockenberg, Palantype dan Numeric.<br><br>

            Berikut ini penjelasan jenis-jenis Keyboard untuk sebuah komputer:<br>

            1. Keyboard QWERTY<br>
            Untuk Keyboard pertama ini mungkin sudah tidak asing lagi bagi Anda. Terdapat urutan qwerty pada tombol huruf baris kedua pada Keyboard ini, membuat hardware ini menjadi tidak asing lagi.<br>

            Tata letak huruf ini mulai ditemukan pada tahun 1878 oleh Christopher Latham Sholes. Selain penemu Keyboard qwerty, Chirstoper juga penemu mesin ketik. Pada tahun 1905 Keyboard qwerty menjadi standar mesin tik komersil.<br><br>

            2. Keyboard Maltron<br>
            Keyboard yang satu ini mungkin jarang Anda lihat. Memiliki bentuk fisik yang sangat berbeda dengan keyboard lainnya, membuat keyboard ini mudah dikenali.<br>

            Pada umumnya keyboard memiliki bidang datar pada fisiknya, tidak dengan keyboard Maltron.

            Keyboard ini memiliki bidang cekung kedalam, sehingga mekanis dalam pengetikan menjadi lebih cepat. Dengan Keyboard ini Anda bisa mengetik dengan 10 jari.<br><br>

            3. Keyboard Dvorak<br>
            Keyboard yang satu ini mulai hadir pada tahun 1933. Tampilan desain yang sama seperti papan dan yang membedakannya dengan keyboard qwerty adalah tata letak huruf.<br>
            Pada keyboard Dvorak, tangan kanan menjadi lebih banyak bergerak di bandingkan dengan tangan kiri. Hal ini karena huruf yang lebih sering digunakan di susun ke arah kanan.<br><br>

            4. Keyboard Alphabetic<br>
            Pernah membayangkan kenapa huruf pada keyboard tidak berurutan saja sesuai dengan alphabet? Mungkin sebagian dari Anda pernah.

            Hal ini juga pernah di pikirkan oleh orang dahulu. Tata letak pada keyboard Alphabetic memang berurutan sesuai dengan huruf pada alphabet.

            Tapi keyboard ini jarang digunakan karena tatak letak yang kurang pas, sehingga memperlambat dalam proses pengetikan.<br><br>

            5. Keyboard Klockenberg<br>
            Keyboard ini hadir setelah Qwerty dan Dvorak, hadir sebagai penyempurna dari keyboard sebelumnya. Yang membedakan keyboard ini terletak pada tampilan yang terbagi menjadi dua bagian.
            <br>
            Dua bagian ini terpisah dengan masing-masing sudut 15 derajat miring ke bawah. Tata letak yang digunakan dalam keyboard ini dianggap dapat meringankan beban otot pada jari dan pergelangan tangan.<br><br>

            6. Keyboard Palantype<br>
            Keyboard Palantype adalah jenis keyboard yang mempunyai tiga bagian, yaitu kanan, tengah dan kiri. Setiap bagian digolongkan dengan masing-masing huruf konsonanya.<br>

            Pada bagain kanan menunjukan kumpulan huruf konsonan terakhir. Dan bagian tengah menujukan kumpulan konsonan huruf tengah dan pada bagian kiri terdapat huruf konsonan awal.

            Tata letak seperti membuat Palantype lebih hemat ruang.<br><br>

            7. Keyboard Numeric<br>
            Keyboard ini lebih sering digunakan untuk menginput data bilangan dalam jumlah yang besar. Tata letaknya yang mudah di jangkau oleh pengguna membuat keyboard ini juga populer di gunakan.

            Selain tata letak yang mudah di jangkau, pengguna juga merasa lebih nyaman dalam proses mengetik. Kebanyakan keyboard ini lebih sering digunakan oleh tangan kanan.<br><br>

            Jenis Keyboard berdasarkan Bentuk<br>
            Zaman yang semakin canggih, membuat bentuk keyboard yang juga beragam. Semakin praktis dalam mengoperasikan benda ini, juga membuat waktu pengguna semakin efesien.

            Mulai dari penggunaan kabel untuk menghubungkannya dengan Central Processing Unit hingga konektivitas melalui Bluetooth.
            <br><br>
            Berikut ini pembahasan tentang jenis keyboard berdasarkan bentuk nya:<br>

            1. Keyboard Serial<br>
            Keyboard Serial merupakan keyboard yang menggunakan DIN 5 pada portnya. Untuk komputer yang berjenis AT sangat cocok untuk keyboard serial.

            Pada port DIN 5 keyboard ini terdapat 5 jarum, sehingga hal ini akan berguna sebagai detektor aktif. Jika salah satu mengalami kerusakan pada port nya maka keyboard ini tidak akan bisa digunakan.<br><br>

            2. Keyboard PS/2<br>
            Jenis keyboard ini tidak jauh berbeda dengan keyboard serial, hanya saja keyboard PS/2 lebih sering dipasangkan udengan sebuah komputer bertype ATX.

            Jenis keyboard ini juga lebih sering digunakan dan dipakai. Jenis port ini menggunakan plug DIN mini yang di dalamnya terdapat 6 jarum sebagai penghubung.

            Dan setiap jarum masing-masing memiliki fungsi dan tugasnya.<br><br>

            3. Keyboard Wireless<br>
            Tidak kebanyakan keyboard pada sebelumnya, keyboard ini menggunakan sambungan tanpa kabel ke sebuah komputer.

            Dengan koneksi yang berbentuk infrared, wifi atau bluetooth, membuat keyboard ini dapat digunakan dengan semua jenis komputer.

            Menggunakan port USB sebagai penghantar sinyal, kemudian sinyal-sinyal tersebut ditangkap langsung oleh Mouse agar dapat terkoneksi.
            <br><br>
            4. Keyboard USB<br>
            Keyboard USB merupakan paling sering dijumpai dan digunakan oleh banyak user. Jenis keyboard ini juga bisa digunakan semua jenis komputer.

            Proses penggunaan pemasangan yang mudah dan simpel, membuat keyboard ini menjadi andalan. Menggunakan konektor berupa USB pada portnya, membuat proses mengantar data lebih cepat.

            Banyaknya jenis dari keyboard yang beragam akan fungsi dan jenis-jenisnya, membuat kita harus memilih keyboard yang sesuai dengan kegunaan dan tentunya nyaman untuk digunakan. Sekian pembahasan tentang keyboard dan keterkaitannya.



