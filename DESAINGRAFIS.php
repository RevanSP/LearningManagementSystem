            Apa itu Desain Grafis ?<br>
            Desain grafis adalah suatu bentuk komunikasi yang dilakukan secara visual. Desain grafis akan memanfaatkan suatu gambar sebagai media dalam penyampaian pesan, informasi yang disampaikan di dalamnya akan dilakukan seefektif mungkin.<br><br>

            Jenis-jenis Desain Grafis ?<br>
            1. Drafterr<br>
            Drafter atau bisa juga dikatakan sebagai perancang memiliki fungsi untuk membuat suatu rancangan bangunan. Tugas seorang drafter adalah berkaitan dengan suatu industri atau arsitektur.
            Untuk menjadi seorang perancang, perlu ketelitian yang tinggi. Oleh sebab itu, seorang perancang harus selalu hati-hati pada pekerjaannya. Beberapa aplikasi yang bisa digunakan pada jenis desain grafis ini adalah Archicad, Autocad dan 3D Revit Architecture.
            <br><br>
            2. Editor<br>
            Jenis desain grafis yang kedua adalah editor. Editor memiliki beberapa fungsi. Seperti membuat banner, membuat cover atau sampul, membuat logo, stiker, poster, kartu nama dan lain sebagainya.

            Editor sering dibutuhkan di dunia publikasi. Umumnya, dunia periklanan juga sering membutuhkan seorang editor. Beberapa aplikasi yang bisa digunakan seorang editor antara lain seperti corel draw, adobe photoshop dan illustrator.
            <br><br>


            3. Layouter<br>
            Layouter adalah jenis desain grafis yang fungsinya membuat tatanan letak di majalah atau koran. Tujuannya supaya lebih enak dipandang.
            Layouter juga sering dibutuhkan di dalam dunia percetakan. Beberapa aplikasi yang bisa digunakan untuk layouter adalah Adobe Page Maker, Ms. Publisher dan Adobe InDesign.
            <br><br>


            4. Art Director<br>
            Art director memiliki fungsi untuk membuat karya seni seperti hiasan atau visual effect. Dalam proses pembuatannya, diperlukan kreativitas yang tinggi. Beberapa aplikasi yang bisa digunakan untuk art director seperti Photo paint, Photoshop, Art creator dan Corel draw.
            <br><br>
            5. Fotografer<br>
            Fotografer adalah jenis desain grafis yang memiliki fungsi untuk pengeditan foto. Dalam hal ini, perlu adanya keahlian yang tinggi di dalam dunia fotografi. Seperti ahli dalam menyunting foto sesuai event yang perfect.
            Sama seperti jenis desain grafis lainnya, fotografer juga membutuhkan kreativitas yang tinggi. Selain itu, dituntut untuk menguasai software aplikasi editing. Fotografer sering dibutuhkan di dalam dunia edit foto, wartawan dan fotografi. Beberapa aplikasi yang dapat digunakan seperti Ieworks san Photo studio.<br><br>



            6. Animator<br>
            Animator adalah jenis desain grafis memiliki fungsi untuk menunjang pekerjaan di dalam bidang motion grafis, film fantasi dan sebuah iklan. Dalam proses pengerjaannya, seorang animator membutuhkan daya tahan yang tinggi.
            Tidak hanya itu, dibutuhkan juga kesabaran, kreativitas dan pengetahuan yang tinggi pula. Seorang animator akan sering dijumpai di dunia pertelevisian, advertising dan perfilman. Aplikasi yang dibutuhkan seperti 3D maya, Adobe Flash, Gif Animator dan After Effect..<br><br>

            7. Visualisator<br>
            Fungsi visualisator dalam desain grafis adalah memberikan sebuah gambaran di suatu karya produk. Produk yang dibuat umumnya berbentuk nyata atau 3 dimensi.
            Setiap orang yang ingin menggunakan jenis desain grafis ini harus memiliki kemampuan otak bagian kanan yang tinggi. Dalam menguasai aplikasi desain grafis, visualisator akan sering dibutuhkan dalam dunia visualisasi produk serta presentasi produk. Beberapa aplikasi yang dapat digunakan yaitu Digital Clay, Autocad dan 3D Max.<br><br>


            8. Video Editor<br>
            Fungsi dari seorang video editor adalah untuk mengedit video atau film sekaligus merangkap sebagai seorang video shooter. Dalam melakukannya, diperlukan imajinasi yang tinggi.
            Video editor sering dibutuhkan di dalam dunia industri musik dan dunia perfilman. Aplikasi yang bisa digunakan seperti After Effect, Sony Vegas dan Video Studio.<br><br>



            9. Integrater Designer<br>
            Fungsinya adalah untuk mengedit video dan film, yang sekaligus akan memerankan sebagai seorang video shooter. Aplikasi yang dapat digunakan seperti Premiere Pro.<br><br>


            Tugas Desain Grafis<br>

            Seseorang yang berkecimpung di bidang desain grafis tidak hanya harus membuat konsep dan berpikir secara visual saja. Ada beberapa tugas yang harus dijalankan. Berikut ini adalah beberapa tugas seorang graphic designer :<br>
            - Menyiapkan bahan yang akan diubah ke bentuk visual.<br>
            - Membuat konsep dan rencana dari semua informasi, dan material yang diberikan oleh seorang klien.<br>
            - Melakukan ilustrasi konsep dengan membuat draft kasar dari copy nya dan ilustrasinya.<br>
            - Menyelesaikan proyek, serta melakukan koordinasi dengan pihak luas. Seperti percetakan, agensi, art service dan lain sebagainya.<br>
            - Melakukan kontribusi dengan tim, untuk mencapai tujuan dari proyek yang akan dikerjakan tersebut.<br>
            - Bertemu dengan seorang art director atau klien. Tujuannya untuk mengetahui lebih jauh mengenai proyek yang diberikan.<br>
            - Melakukan brainstorming bersama para klien.<br>
            - Memberikan masukan pada desain yang ingin dibuat, sesuai dengan audiens atau target market yang ingin dijadikan target.<br>
            - Membuat ilustrasi atau gambar yang berfungsi untuk mengidentifikasi pesan dari suatu produk.<br>
            - Membuat bentuk grafis, seperti logo, website, dan ilustrasi produk.<br>
            - Memilih layout, gambar, warna, dan font.<br>
            - Mempresentasikan hasil ilustrasi yang telah dibuat kepada art director dan klien.<br>
            - Melakukan revisi dari para klien.<br>
            - Melakukan quality check pada ilustrasi sebelum dicetak dan dipublikasikan.<br>
